// Understanding JOINS (Theoretical)
// Explain the difference between INNER JOIN, LEFT JOIN, RIGHT JOIN, and FULL OUTER JOIN with real-world examples.

// ANS: 

// INNER JOIN →  take only commen elements from both tables.
// LEFT JOIN →  take only from left tables, priority for left table.
// RIGHT JOIN → take only from right tables, priority for right table.
// FULL OUTER JOIN → take from both tables, priority for both tables.

// Real-World Example (Customers & Orders)
// INNER JOIN → Customers who placed orders
// LEFT JOIN → All customers, even if no orders
// RIGHT JOIN → All orders, even if customer deleted
// FULL OUTER JOIN → All customers & orders, even if unrelated






